function updateArticle(locale, slug, body) {
  const filePath = path.join(root, 'content', 'tutorials', locale, 'bootcamp', `${slug}.mdx`);
  const raw = fs.readFileSync(filePath, 'utf8');
  const stripped = raw.replace(new RegExp(`\\n?${markerStart}[\\s\\S]*?${markerEnd}\\n?`, 'm'), '\n');
  const next = `${stripped.trim()}\n\n${markerStart}\n${body.trim()}\n${markerEnd}\n`;
  fs.writeFileSync(filePath, next, 'utf8');
}

