function codeBlock(lang, lines) {
  return ['```' + lang, ...lines, '```', ''];
}

function lines(...items) {
  return items.flat().join('\n');
}

function writeSvg(day, title, steps) {
  const width = 1200;
  const height = 630;
  const colors = ['#2563eb', '#059669', '#d97706', '#7c3aed', '#dc2626'];
  const stepCards = steps.map((step, index) => {
    const y = 190 + index * 72;
    const color = colors[index % colors.length];
    return `  <rect x="110" y="${y}" width="980" height="52" rx="8" fill="#ffffff" stroke="#d1d5db"/>
  <circle cx="145" cy="${y + 26}" r="16" fill="${color}"/>
  <text x="145" y="${y + 32}" text-anchor="middle" font-size="18" font-weight="700" fill="#ffffff">${index + 1}</text>
  <text x="180" y="${y + 33}" font-size="24" font-weight="650" fill="#111827">${escapeXml(step)}</text>`;
  }).join('');

  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
  <rect width="${width}" height="${height}" fill="#f8fafc"/>
  <rect x="70" y="70" width="1060" height="490" rx="14" fill="#eef2ff" stroke="#c7d2fe"/>
  <text x="110" y="125" font-size="26" font-weight="700" fill="#4338ca">HermesAgent 101</text>
  <text x="110" y="165" font-size="40" font-weight="800" fill="#111827">${escapeXml(title)}</text>
${stepCards}
  <text x="110" y="545" font-size="20" fill="#4b5563">Beginner flow: follow one step, check the expected result, then continue.</text>
</svg>
`;

  const outPath = path.join(root, 'public', 'images', 'bootcamp', `beginner-day-${day}.svg`);
  fs.writeFileSync(outPath, svg, 'utf8');
}

function escapeXml(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

